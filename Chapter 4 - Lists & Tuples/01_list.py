#Create a list using []
a = [1,2,3,7,66]

#print the list using print() function
print(a)

#Access using index using a[0], a[1], ....
print(a[2])

#Changing the value of the list
a[0] = 777
print(a)

#We can create a list with items of different type
b = [77,"Root",False,6.9]
print(b)

#List Slicing
friends = ["Root","Groot","Sam","Alex",99]
print(friends[0:3])
print(friends[-4:])

